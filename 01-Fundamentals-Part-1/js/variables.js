//Camel Case
/**
 * Camel case is the practice of writing phrases without spaces or punctuation. The format indicates the separation of words with a single capitalized letter, and the first word starting with either case.
 */

let myName = "Fernando";
let myAge = 23;
let myHeight = 1.6;

console.log(
  `My name is ${myName}, I'm ${myAge} years old and my height is ${myHeight} m tall.`
);

