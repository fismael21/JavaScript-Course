//Conversion
const inputYear = "1999";
console.log(inputYear + 18); //Output: 199918

console.log(Number(inputYear) + 18); //Output: 2017

console.log(typeof inputYear, typeof Number(inputYear)); //Output: string, number

console.log(Number("Fernando")); //Output: NaN
console.log(typeof NaN); //Output: number

console.log(String(23), 23);
